<template>
  <div>
    <template v-if="loading">
      <spinner />
    </template>
    <template v-else>
      <table>
        <thead>
        <tr>
          <th>Number</th>
          <th>Date Ordered</th>
          <th>Order Placed By</th>
          <th>Debug</th>
        </tr>
        </thead>
        <tbody>
        <tr v-for="order in orders.data">
          <td>{{ order.number }}</td>
          <td>
            <template v-if="order.dateOrdered && order.dateOrdered.date">
              {{ order.dateOrdered.date }}
            </template>
          </td>
          <td>â€”</td>
          <td>
            <pre>{{order}}</pre>
          </td>
        </tr>
        </tbody>
      </table>
    </template>

    <div class="mt-4 text-gray-500 text-sm">{{orders.total}} rows</div>
  </div>
</template>

<script>
import {mapGetters, mapState} from 'vuex';
import Spinner from '../../../../common/ui/components/Spinner';

export default {
  components: {Spinner},
  data() {
    return {
      loading: true,
    }
  },
  computed: {
    ...mapState({
      orders: state => state.organizations.orders,
    }),
    ...mapGetters({
      currentOrganization: 'organizations/currentOrganization',
    }),
  },

  mounted() {
    this.$store.dispatch('organizations/getOrders', {
        organizationId: this.currentOrganization.id
      })
      .then(() => {
        this.loading = false
      });
  }
}
</script>